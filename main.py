app = FastAPI(title="ELI5 AI Explainer")


@app.on_event("startup")
def on_startup():
    init_db()


# Serve static files and the SPA entry point
app.mount("/static", StaticFiles(directory="static"), name="static")


@app.get("/")
def index():
    return FileResponse("static/index.html")


# ---------------------------------------------------------------------------
# Pydantic models
# ---------------------------------------------------------------------------
class ExplainRequest(BaseModel):
    term: str


class ExplainResponse(BaseModel):
    term: str
    explanation: str


# ---------------------------------------------------------------------------
# Routes
# ---------------------------------------------------------------------------


@app.post("/api/explain", response_model=ExplainResponse)
async def explain(req: ExplainRequest):
    term = req.term.strip()

    # Generate explanation locally (no LLM needed)
    explanation = generate_explanation(term)

    # Save to SQLite
    with get_db() as conn:
        conn.execute(
            "INSERT INTO explanations (term, explanation) VALUES (?, ?)",
            (term, explanation),
        )

    return {"term": term, "explanation": explanation}


@app.get("/api/history")
async def history():
    with get_db() as conn:
        rows = conn.execute(
            "SELECT id, term, explanation FROM explanations ORDER BY id DESC"
        ).fetchall()

    return [{"id": r["id"], "term": r["term"], "explanation": r["explanation"]} for r in rows]


